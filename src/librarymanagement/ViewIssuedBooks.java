    public ViewIssuedBooks() {
        try{
            Con con = new Con(); 
            String sql = "select studentID,bookID,studentName,phone,issueDate,returnDate from issueBook";
            PreparedStatement st = con.c.prepareStatement(sql,ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();
            int cols=rsmd.getColumnCount();
            column=new String[cols];
            for(int i=1;i<=cols;i++){
		column[i-1]=rsmd.getColumnName(i).toUpperCase();
            }
			
            rs.last();
            int rows=rs.getRow();
            rs.beforeFirst();

            data=new String[rows][cols];
            int count=0;
            while(rs.next()){
                for(int i=1;i<=cols;i++){
                    data[count][i-1]=rs.getString(i);
                }
		count++;
            }
            initComponents();
        } catch (SQLException e) {
        }
    }

